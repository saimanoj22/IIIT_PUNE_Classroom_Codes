// This program assigns and displays different data types like:
//  byte, int, long, float, double, char and string

public class DataTypes
{
	public static void main(String[] args) {
		byte byteNum = 100;
		short shortNum = 5000;
		int intNum = 1000000;
		long longNum = 100000000000l;
		float floatNum = 2.5f;
		double doubleNum = 4.6253d;
		char a = 'A';
		String str = "Hello";
		
		System.out.println("byte : " + byteNum);
		System.out.println("short : " + shortNum);
		System.out.println("int : " + intNum);
		System.out.println("long : " + longNum);
		System.out.println("float : " + floatNum);
		System.out.println("double : " + doubleNum);
		System.out.println("char : " + a);
		System.out.println("String : " + str);
	}
}
